from sqlalchemy import Column, Integer, String, Date, Time
from database import Base

class Booking(Base):
    __tablename__ = "bookings"

    id = Column(Integer, primary_key=True, index=True)
    name = Column(String)
    email = Column(String)
    service = Column(String)
    date = Column(Date)
    time = Column(Time)
    notes = Column(String)